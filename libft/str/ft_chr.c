#include "../libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] || c == '\0')
	{
		if (s[i] == (c % 256))
			return ((char *)(s + i));
		i++;
	}
	return (0);
}
